def solution(str_list):
    for i, s in enumerate(str_list):
        if s == 'l':
            return str_list[:i]
        elif s == 'r':
            return str_list[i+1:]
    return []
'''
✦ 이 solution 함수는 문자열들이 들어있는 리스트(str_list)를 받아서
  특정 조건에 따라 리스트의 일부를 반환하는 함수입니다.

  동작 방식을 쉽게 설명하면 다음과 같습니다.


   1. 리스트를 처음부터 순서대로 훑어봅니다.
   2. 그러다가 'l' 이라는 글자를 먼저 만나면, 그 글자 왼쪽에 있는
      모든 것들을 결과로 돌려줍니다.
   3. 만약 'r' 이라는 글자를 먼저 만나면, 그 글자 오른쪽에 있는
      모든 것들을 결과로 돌려줍니다.
   4. 리스트 끝까지 훑어봤는데 'l'과 'r'이 하나도 없으면, 그냥 빈
      리스트([])를 돌려줍니다.

  예시:


   * str_list가 ["a", "b", "c", "l", "d", "e"] 라면:
       * 'l'을 만났으므로 그 왼쪽의 ["a", "b", "c"]를 반환합니다.
   * str_list가 ["a", "b", "r", "c", "d"] 라면:
       * 'r'을 만났으므로 그 오른쪽의 ["c", "d"]를 반환합니다.
   * str_list가 ["x", "y", "z"] 라면:
       * 'l'과 'r'이 없으므로 빈 리스트 []를 반환합니다.
'''
